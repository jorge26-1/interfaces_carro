    private void cargarUsuarios() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("Nombre");
    modelo.addColumn("Correo");
    modelo.addColumn("Contraseña");

    // Cambia esto según el nombre de tu tabla
    javax.swing.JTable tabla = tablaUsuarios; // o jTable1 si no lo renombraste

    java.util.ArrayList<String[]> lista = Modelo.RegistroUsuarios.obtenerUsuarios();
    for (String[] usuario : lista) {
        modelo.addRow(usuario);
    }

    tabla.setModel(modelo);
}

